// 年度キー（"r7" / "h22"）→ 表示ラベル（"令和7年(2025年)" / "平成22年(2010年)"）
export function yearKeyToLabel(key) {
  const n = parseInt(key.slice(1), 10);
  if (key.startsWith("r")) {
    return `令和${n}年(${2018 + n}年)`;
  }
  return `平成${n}年(${1988 + n}年)`;
}
